#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub(crate) enum CardTarget {
    Enemy,
    SelfOnly,
    AllEnemies,
}

#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash)]
pub(crate) enum AxisKind {
    Focus,
    Rhythm,
    Momentum,
}

#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash)]
pub(crate) enum CardArchetype {
    Pressure,
    Bulwark,
    Momentum,
    Fabricate,
    Sweep,
    Burst,
}

#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub(crate) struct CardRequirement {
    pub(crate) axis: AxisKind,
    pub(crate) threshold: i8,
}

#[derive(Clone, Copy, Debug, Default, PartialEq, Eq)]
pub(crate) struct CardTraits {
    pub(crate) temporary: bool,
    pub(crate) piercing: bool,
}

#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub(crate) struct CardDef {
    pub(crate) id: CardId,
    pub(crate) name: &'static str,
    pub(crate) cost: u8,
    pub(crate) target: CardTarget,
    pub(crate) description: &'static str,
    pub(crate) archetype: CardArchetype,
    pub(crate) reward_tier: Option<RewardTier>,
    pub(crate) traits: CardTraits,
}

#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub(crate) enum RewardTier {
    Combat,
    Elite,
    Boss,
}

#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub(crate) struct ShopOffer {
    pub(crate) card: CardId,
    pub(crate) price: u32,
}

