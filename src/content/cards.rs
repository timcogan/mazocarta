pub(crate) fn starter_deck() -> Vec<CardId> {
    let mut cards = Vec::with_capacity(12);
    cards.extend(std::iter::repeat_n(CardId::FlareSlash, 5));
    cards.extend(std::iter::repeat_n(CardId::GuardStep, 4));
    cards.extend(std::iter::repeat_n(CardId::Slipstream, 2));
    cards.push(CardId::SunderingArc);
    cards
}

pub(crate) fn card_slug(id: CardId) -> &'static str {
    card_data(id).slug
}

pub(crate) fn resolve_card_slug(slug: &str) -> Option<CardId> {
    KNOWN_CARD_CATALOG
        .iter()
        .copied()
        .find(|card| card_data(*card).slug == slug)
}

pub(crate) fn reward_choices(seed: u64, tier: RewardTier, level: usize) -> Vec<CardId> {
    let mut cards = reward_pool(tier, level);
    cards.sort_by_key(|card| reward_roll_key(seed, *card));
    choose_distinct_archetypes(cards, 3)
}

pub(crate) fn shop_offers(seed: u64, level: usize) -> Vec<ShopOffer> {
    let mut chosen_cards = Vec::new();
    let mut chosen_archetypes = Vec::new();
    let mut offers = Vec::with_capacity(3);

    for (index, tier) in shop_offer_tiers(level).into_iter().enumerate() {
        let offer_seed = seed ^ (index as u64 + 1).wrapping_mul(0xD6E8_FD50_19E3_7C4B);
        let mut cards = reward_pool(tier, level);
        cards.sort_by_key(|card| reward_roll_key(offer_seed, *card));
        let card = cards
            .iter()
            .copied()
            .find(|card| {
                !chosen_cards.contains(card)
                    && !chosen_archetypes.contains(&card_def(*card).archetype)
            })
            .or_else(|| {
                cards
                    .iter()
                    .copied()
                    .find(|card| !chosen_cards.contains(card))
            })
            .or_else(|| cards.first().copied())
            .expect("shop tier should always have at least one card");
        chosen_archetypes.push(card_def(card).archetype);
        chosen_cards.push(card);
        offers.push(ShopOffer {
            card,
            price: shop_price_for_tier(tier),
        });
    }

    offers
}

fn choose_distinct_archetypes(cards: Vec<CardId>, limit: usize) -> Vec<CardId> {
    let mut chosen = Vec::with_capacity(limit);
    let mut archetypes = Vec::with_capacity(limit);

    for card in &cards {
        let archetype = card_def(*card).archetype;
        if !archetypes.contains(&archetype) {
            chosen.push(*card);
            archetypes.push(archetype);
            if chosen.len() == limit {
                return chosen;
            }
        }
    }

    for card in cards {
        if !chosen.contains(&card) {
            chosen.push(card);
            if chosen.len() == limit {
                break;
            }
        }
    }

    chosen
}

pub(crate) fn reward_pool(tier: RewardTier, level: usize) -> Vec<CardId> {
    let unlocked_level = level.clamp(1, 3);
    all_base_cards()
        .iter()
        .copied()
        .filter(|card| card_def(*card).reward_tier == Some(tier))
        .filter(|card| {
            if tier == RewardTier::Elite {
                card_data(*card).elite_unlock_level <= unlocked_level
            } else {
                true
            }
        })
        .collect()
}

fn shop_offer_tiers(level: usize) -> [RewardTier; 3] {
    match level.clamp(1, 3) {
        1 => [RewardTier::Combat, RewardTier::Elite, RewardTier::Elite],
        2 => [RewardTier::Combat, RewardTier::Elite, RewardTier::Boss],
        _ => [RewardTier::Elite, RewardTier::Elite, RewardTier::Boss],
    }
}

fn shop_price_for_tier(tier: RewardTier) -> u32 {
    match tier {
        RewardTier::Combat => 16,
        RewardTier::Elite => 24,
        RewardTier::Boss => 40,
    }
}

